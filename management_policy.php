1. 会員相互の支援<br>
会員相互の支援体制を構築するには<br>
＊自分たちと同じ境遇の人と連携を取る<br>
＊患者・家族に対する心理的サポートを行なう(疾患の受容など)</div>
患者会によるピアサポート<br>
患者会の会員は「疾患受容」の貴重な経験者である<br>
患者会は新規患者の窓口となる<br>
疾患の告知から受容までのプロセス<br>
時間の経過とともに心理は変化する<br>
1段階　ショック<br>
2段階　否認<br>
3段階　悲しみと怒り どうして家にそんなことが<br>
4段階　適応<br>
5段階　再起 この子を守ってあげようとする意識<br>
時間軸は人により変わるが、特にポンぺのような希少疾患は1～3段階が強いかもしれない。<br>
<div class="bold">＊生活支援関連情報を共有していく(医療費助成、福祉政策)</div>
医師の専門外のことを患者団体が相互にやっていくことが大事<br>
患者会は社会的使命の一つであり、このコミュニティーに入ることにより、「自分は一人ではない」と思えれば成功だと言える。<br>
<br>
<div class="bold">
2. 研究協力・研究支援<br>
希少疾患研究の実情<br>
＊研究者が少ない<br>
＊新薬開発や新規治療法の開発が進まない<br>
＊患者数が少なく、臨床研究や統計的処理がなじまない</div>
酵素補充療法が開発されて、これまで2歳で死亡していた乳児型が生存できるようになった。<br>
だが一方で、限界があることにも気付き始めており、そのため、新しい治療法についての研究・協力が必要となる。しかし、残念ながら希少疾患の研究者というのは大変少ない、その上、患者数も少ないというのが問題である。<br>
<br>
<div class="bold">ポンペ病研究の課題<br>
＊現行の酵素補充療法を評価すること</div>
ガイドラインの作成<br>
<div class="bold">＊新しい治療方法の開発<br>
＊発症前診断の実施(新生児マススクリーニング: NBS、ハイリスクスクリーニング)</div>
早期発見することの意味が大きい<br>
新生児マススクリーニング: NBS<br>
NBS推奨疾患(アメリカ)<br>
・ポンぺ病<br>
・原発性免疫不全症候群<br>
・ムコ多糖症I型<br>
・副腎白質ジストロフィー<br>
上記4疾患は早期発見・早期治療することにより確実に患者さんへの利益が実証されている。<br>
ハイリスクスクリーニング<br>
・ハイリスクスクリーニングは新生児に対し何かしらの異常を疑った時は検査してみることで発見につながる。<br>
・国立成育医療研究センターで実施しているポンペ病ハイリスクスクリーニングの結果(2009年～2015年)は、2009年1例、2010年1例、2011年3例、2012年2例、2013年3例、2014年5例、2015年8例と年々患者数は増えている。<br>
