<h2>特定非営利活動法人「全国ポンペ病患者と家族の会」設立趣旨</h2>
<p class="text">
ポンペ病は、細胞内酵素である酸性α-グルコシダーゼの欠損により、あらゆる細胞のライソゾームにグリコーゲンが大量に蓄積する病気であり、長く有効な治療法がありませんでしたが、欠損酵素の補充を目的としたGenzyme社治療薬「マイオザイム」が開発されたことにより、近年最も劇的な治療上の進歩を遂げた疾患の一つです。<br>
